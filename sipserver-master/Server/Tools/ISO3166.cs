		static ISO3166()
		{
			itemsByNumbers = new ISO3166Item[1000];

			foreach (var item in items)
				if (item.Number < itemsByNumbers.Length)
					itemsByNumbers[item.Number] = item;
		}

		public static string ToA2(int number, string defaultValue)
		{
			ISO3166Item item = null;

			if (number < itemsByNumbers.Length)
				item = itemsByNumbers[number];

			if (item != null)
				return item.A2;

			return defaultValue;
		}

		class ISO3166Item
		{
			public ISO3166Item(string country, string a2, string a3, int number)
			{
				Country = country;
				A2 = a2;
				A3 = a3;
				Number = number;
			}

			public string Country { get; private set; }
			public string A2 { get; private set; }
			public string A3 { get; private set; }
			public int Number { get; private set; }
		}
